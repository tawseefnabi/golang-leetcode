/*
21. Merge Two Sorted Lists
Source: https://leetcode.com/problems/merge-two-sorted-lists/
Merge two sorted linked lists and return it as a new list. The new list should be made by splicing together the nodes of the first two lists.
Example:
Input: 1->2->4, 1->3->4
Output: 1->1->2->3->4->4
*/
package mergetwosortedarrays

// ListNode is node of linked list

type ListNode struct {
	Val  int
	Next *ListNode
}

func MergeTwoArrays(l1 *ListNode, l2 *ListNode) *ListNode {
	if l1 == nil && l2 == nil {
		return nil
	}
	if l1 == nil {
		return l2
	}
	if l2 == nil {
		return l1
	}
	if l1.Val < l2.Val {
		l1.Next = MergeTwoArrays(l1.Next, l2)
		return l1
	}
	l2.Next = MergeTwoArrays(l1, l2.Next)
	return l2
}
